package calendar;
public class calDTO {
	private String calOldTitle;
	private String calId;
	private String calName;
	private String calTitle;
	private String calStartDate;
	private String calEndDate;
	private String calText;
	
	public String getCalOldTitle() {
		return calOldTitle;
	}
	public void setCalOldTitle(String calOldTitle) {
		this.calOldTitle = calOldTitle;
	}
	public String getCalId() {
		return calId;
	}
	public void setCalId(String calId) {
		this.calId = calId;
	}
	public String getCalName() {
		return calName;
	}
	public void setCalName(String calName) {
		this.calName = calName;
	}
	public String getCalTitle() {
		return calTitle;
	}
	public void setCalTitle(String calTitle) {
		this.calTitle = calTitle;
	}
	public String getCalStartDate() {
		return calStartDate;
	}
	public void setCalStartDate(String calStartDate) {
		this.calStartDate = calStartDate;
	}
	public String getCalEndDate() {
		return calEndDate;
	}
	public void setCalEndDate(String calEndDate) {
		this.calEndDate = calEndDate;
	}
	public String getCalText() {
		return calText;
	}
	public void setCalText(String calText) {
		this.calText = calText;
	}
	
	
}
